def test_polar_alignment_error_matches_a_direct_altaz_conversion():
    when = datetime(2026, 3, 20, 12, 0, 0, tzinfo=timezone.utc)
    lat_deg, lon_deg = 46.18, 6.14
    axis_ra_deg, axis_dec_deg = 10.0, 89.5  # half a degree off the pole
    az_deg, alt_deg = equatorial_to_altaz(axis_ra_deg, axis_dec_deg, lat_deg, lon_deg, when)

    result = polar_alignment_error(axis_ra_deg, axis_dec_deg, lat_deg, lon_deg, when)

    assert result.axis_alt_deg == pytest.approx(alt_deg)
    assert result.axis_az_deg == pytest.approx(az_deg)
    assert result.error_alt_deg == pytest.approx(alt_deg - lat_deg)
    expected_az_error_deg = ((az_deg - 0.0 + 180.0) % 360.0) - 180.0
    assert result.error_az_deg == pytest.approx(expected_az_error_deg)
    assert result.error_deg > 0.0


